public class TypeCasting {
    public static void main(String[] args) {
        //type means datatype ----> converting datatypes (from one to another one) means casting
        //automatic casting
        //upcasting
        //downcasting

        //example -1 upcasting
        int a = 5;
        float f = a;

        //example -2 downcasting
        float fl = 5.3f;
        int b = (int) fl;
        System.out.println(b);
    }
}
